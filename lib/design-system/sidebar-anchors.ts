// --- Anomalies -----------------------------------------------------
// Only show categories that currently have at least one entry —
// otherwise the sub-item is dead weight.
export function anomalyAnchors(): Anchor[] {
  const path = "/design-system/anomalies"
  const present = new Set(anomalies.map((a) => a.category))
  const order: AnomalyCategory[] = [
    "color",
    "typography",
    "structure",
    "iconography",
    "motion",
    "external",
  ]
  return order
    .filter((c) => present.has(c))
    .map((c) => anchor(path, anomalyCategoryInfo[c].name))
}
